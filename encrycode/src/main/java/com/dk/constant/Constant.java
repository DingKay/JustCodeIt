package com.dk.constant;

/**
 * @author DingKay
 */
public interface Constant {
    /**
     * zero width space char
     * Unicode : U+200B
     */
    char UNICODE_MIRROR_ZERO = '\u200B';
    
    /**
     * zero width non-joiner
     * Unicode : U+200C
     */
    char UNICODE_MIRROR_ONE = '\u200C';
    
    /**
     * zero width joiner
     * Unicode : U+200D
     */
    char UNICODE_MIRROR_SEGMENT = '\u200D';

    /**
     * binary char : 0
     */
    char ZERO = '0';

    /**
     * binary char : 1
     */
    char ONE = '1';
}
